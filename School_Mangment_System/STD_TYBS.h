
#ifndef  STD_TYBS_H
#define  STD_TYBS_H
  
typedef unsigned char            u8;//1 byte
typedef signed char              s8;  //1byte
typedef unsigned short int       u16;  //2byte
typedef signed short int         s16;
typedef unsigned long int        u32; //4byte
typedef signed long int          s32;
typedef unsigned long long int   u64;   //byte
typedef signed long long int      s64;
typedef float                     f32;
typedef double                     f64;

#endif 